#Підрахувати кількість елементів одновимірного масиву, для яких
#виконується нерівність i*i<ai<i!
#Драч Ольга Олескандрівна, 122Д

import math
g = 0
for j in range(10):   #Дапазон
    x = math.factorial(j)   #Факторіал іерції
    z = j * j   #Квадртат ітерації
    v = int(input('Введіть число:'))  #Масив
    print(f'{x} меньше за v')
    print(f'{z} більше за v')
    if z < v < x:   #Умова
        g += 1   #Лічильник
print('Кількість елементів: ', g)

#Дан одновимірний масив цілих чисел. Знайдіть, скільки разів в ньому
#повторюється найчастіше число.
#Драч Ольга Олескандрівна, 122Д

v = 0
r = [8,13,21,55,898,13,21,34,55,89]
for b in range(len(r)):
    m = r.count(r[b])   #Кількість зустрічей певного ітеріруемого об'єкта
    if m > v:   #Якщо ця кількість більше того, що був раніше, то перезаписуємо найбільший
        v = m
print(v,'раз(и/ів) повторюється найчастіше число.')

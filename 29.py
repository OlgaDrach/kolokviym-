from random import randint
n = 24
m = int(input('Введіть число:'))
for i in range(5):   #Діапазон
    g = randint(10,20)   #Масив
for i in range(10):   #Діапазон
    g = randint(1,15)
    if g%2 == 0:      #Умова
        n += 1      #Лічильник
    if g == m:
        break
print('Кількість парних елементів',n)

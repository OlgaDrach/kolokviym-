#Задано два натуральних числа a і b. Змінній w привласнити значення
#істина, якщо в одновимірному цілочисельному масиві є хоча б один елемент, кратний а
#і не кратний b.
#Драч Ольга Олескандрівна, 122Д

u = int(input('Введіть число:'))
g = int(input('Введіть число:'))
r = False
for j in range(10):   #Діапазон
    e = int(input('Введіть числa:'))
    if e % u == 0 and e % g != 0:   #Умова
        r = True
print('Змінна набула значення:', r)

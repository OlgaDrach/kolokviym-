import numpy as np
import random
j = 0
a = np.zeros(20, dtype=int)
for i in range(20):  #Діапазон
    a = random.randint(100, 200)  #Масив 
for i in range(20):
    if i % 2 != 0:   #Умова
        j += j + a    #Сума
print(j)

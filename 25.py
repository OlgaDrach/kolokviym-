import numpy as np
from random import randint
j = 0
h = np.zeros(10, dtype=int)   #Замінює значення нулями
for i in range(10):    #Діапазон
    h = randint(10, 100)
print(h)
for i in range(10):    #Діапазон
    if h % 5 == 0:   #Умова
        j += h    #Створення
print(j)

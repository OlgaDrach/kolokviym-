a = False 
b = [89,144,233,377,610,987,1597,2584,4181,6765,10946]   #Масив
for c in range(len(b)-2):     #Діапазон без 2 останніх так як виходить з діапазону
    if b[c] == b[c+1] == b[c+2]:   #Перевірка
        a = True
print('Змінна набуває значення: ',a)

#У лінійному масиві знайти максимальний елемент. Вставте порядковий
#номер елемента за ним, пересунувши всі залишилися на одну позицію вправо.
#Драч Ольга Олескандрівна, 122Д

n = [3,7,3,9,5,6,212,1,2]   #Масив
v = list()
q = max(n)   #Шукаємо максимальний елемент
for c in range(len(n)):  #Прохидомось по масиву
    v.append(n[c])   #Заносимо до іншого масиву
    if n[c] == q:   #Якщо умова виконалась то ставимо після максимального
        v.append(c)    #Додаємо все сюди
print(v)

from random import randint
t = False    #змінна яка при дотриманні певного умови стає True
for i in range(10):  #Діапазон
    r = randint(10, 20)  #Масив
    if r < 0 and r % 2 == 0:   #Умова
            t = True
            break
print('Змінна набула значення:', t)
